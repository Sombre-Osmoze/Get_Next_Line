#include <stdlib.h>

int	ft_strequ(const char *s1, const char *s2)
{
	if (s1 != NULL && s2 != NULL)
		while (*s1 || *s2)
		{
			if (*s1 != *s2)
				return (0);
			s1++;
			s2++;
		}
	return (1);
}
